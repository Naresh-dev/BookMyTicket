package com.honeywell.bookMyTicket;

/**
 * @author Hackathon
 *
 */
public class Show {
	
	private Long id;
	private String name;
	private String movieName;
	private Long theaterId;
	private Long screenNumber;
	private String desc;
	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the movieName
	 */
	public String getMovieName() {
		return movieName;
	}
	/**
	 * @param movieName the movieName to set
	 */
	public void setMovieName(String movieName) {
		this.movieName = movieName;
	}
	/**
	 * @return the theaterId
	 */
	public Long getTheaterId() {
		return theaterId;
	}
	/**
	 * @param theaterId the theaterId to set
	 */
	public void setTheaterId(Long theaterId) {
		this.theaterId = theaterId;
	}
	/**
	 * @return the screenNumber
	 */
	public Long getScreenNumber() {
		return screenNumber;
	}
	/**
	 * @param screenNumber the screenNumber to set
	 */
	public void setScreenNumber(Long screenNumber) {
		this.screenNumber = screenNumber;
	}
	/**
	 * @return the desc
	 */
	public String getDesc() {
		return desc;
	}
	/**
	 * @param desc the desc to set
	 */
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	
	

}
